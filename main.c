int check_true_x_position(int iAMapPosition) {
	int iTruePosition = (iAMapPosition % 37) * 16;
	
	return iTruePosition;
}
int check_true_y_position(int iAMapPosition) {
	int iTruePosition = (iAMapPosition / 37) * 16;

	return iTruePosition;
}

int main(void)
{
//------------------------------------\\\WINDOW\\\----------------------------------------//

    const int iScreenWidth = 592;
    const int iScreenHeight = 592;

    InitWindow(iScreenWidth, iScreenHeight, "RAYLIB");

    eGameScreen currentScreen = TITLE;

    SetTargetFPS(60);               

//------------------------------------\\\WINDOW\\\----------------------------------------//

    sEntitie oPlayer = {16, 16};

    sEntitie* aEnemyList = malloc(sizeof(sEntitie));
    int iEnemyNumber = 0;

    while (!WindowShouldClose())    
    {
        switch (currentScreen)
        {
            case TITLE:
            {
                if (IsKeyPressed(KEY_SPACE) || IsGestureDetected(GESTURE_TAP))
                {
                    currentScreen = GAMEPLAY;
                }
            } break;
            case GAMEPLAY:
            {
                if (IsKeyPressed(KEY_SPACE) || IsGestureDetected(GESTURE_TAP))
                {
                	currentScreen = TITLE;
		}
		else if (IsKeyPressed(KEY_W))
			{
			    oPlayer.iPosY -= 16;
			} else if (IsKeyPressed(KEY_A))
			{
		    	    oPlayer.iPosX -= 16;
			} else if (IsKeyPressed(KEY_S))
			{
		    	    oPlayer.iPosY += 16;
			} else if (IsKeyPressed(KEY_D))
			{
			    oPlayer.iPosX += 16;
			}
            	} break;
            	default: break;

	}

//------------------------------------\\\DRAW\\\----------------------------------------//

	BeginDrawing();

            ClearBackground(RAYWHITE);

            switch(currentScreen)
            {
                case TITLE:
                {
		    DrawRectangle(0, 0, iScreenHeight, iScreenWidth, BLACK);
                    DrawRectangle(200, 260, 200, 100, WHITE);
                    DrawText("TESTE RAYLIB", 220, 300, 20, BLACK);
                } break;

                case GAMEPLAY:
       		{

		    for(int i = 0; i < 1369; i++) {
			    
			switch(aMap[i])
			    {
			    
			    case 2:
			    	{
		            		int aTrueSquarePosition[] = {check_true_x_position(i), check_true_y_position(i)}; 
					iEnemyNumber = iEnemyNumber + 1;
					sEntitie oEnemy = {aTrueSquarePosition[0], aTrueSquarePosition[1]};
		    			DrawRectangle(oEnemy.iPosX, oEnemy.iPosY, 16, 16, RED);
					aEnemyList = realloc(aEnemyList, (iEnemyNumber * sizeof(sEntitie)));
			    	} break;
			    
			    case 3:
				{
		            		int aTrueSquarePosition[] = {check_true_x_position(i), check_true_y_position(i)}; 
					DrawRectangle(aTrueSquarePosition[0], aTrueSquarePosition[1], 16, 16, BLUE);
				} break;
			    
			    default: break;
			    }
		    } 

		    DrawRectangle(oPlayer.iPosX, oPlayer.iPosY, 16, 16, GREEN);

		    int iLines = 0;
		    for (int i = 0; i < 37; i++) {
			DrawLine(0, iLines, iScreenWidth, iLines, BLACK);
		    	DrawLine(iLines, 0, iLines, iScreenHeight, BLACK);
		        iLines = iLines + 16;	
		    }

                } break;

                default: break;
            }

        EndDrawing();
    }

//------------------------------------\\\DRAW\\\----------------------------------------//

    CloseWindow();        
    return 0;
}
